public class VariableExercise {
    public static void main(String[] args) {
        int foo = 4;
        System.out.println(foo);

        double bar = 7.7;
        System.out.println(bar);

        String foo_bar = "Apple";
        System.out.println(foo_bar);

        boolean foobar = true;
        System.out.println(foobar);
    }
}
