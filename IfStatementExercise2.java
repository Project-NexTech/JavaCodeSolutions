public class IfStatementExercise2 {
    public static void main(String[] args) {
        int foo = 12;

        if (foo < 10) {
            System.out.println("foo is less than 10");
        } else if (foo <= 12) {
            System.out.println("foo is less than or equal to 12, and greater or equal to 10");
        } else {
            System.out.println("foo is not greater than 12");
        }

    }

}
